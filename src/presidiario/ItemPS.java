package presidiario;

public class ItemPS {
	private int codigo, idade;
	private String nome, presidio;
	
	public ItemPS(int codigo, int idade, String nome, String presidio) {
		this.codigo = codigo;
		this.idade = idade;
		this.nome = nome;
		this.presidio = presidio;
	}

	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}

	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}

	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getPresidio() {
		return presidio;
	}
	public void setPresidio(String presidio) {
		this.presidio = presidio;
	}
}
